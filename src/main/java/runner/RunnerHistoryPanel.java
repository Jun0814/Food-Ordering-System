    public RunnerHistoryPanel(String deliveryId) {
        initComponents();
        this.deliveryId = deliveryId;
        List<Delivery> deliveries = read.readDelivery(delivery.getFilepath());
        for(Delivery delivery : deliveries){
            if(delivery.getDeliveryID().equals(deliveryId)){
                this.deliveryId = delivery.getDeliveryID();
                this.runnerId = delivery.getRunnerID();
                this.description = delivery.getDescription();
                this.date = delivery.getDatetime().split("T")[0];
                this.time = delivery.getDatetime().split("T")[1];
                this.status = delivery.getStatus();
                this.totalAmount = String.valueOf(delivery.getDeliveryFees());
                
            }
        }
        deliveryIdLabel.setText("Delivery #" + deliveryId);
        dateLabel.setText(date);
        timeLabel.setText(time);
        totalAmountLabel.setText("RM" +totalAmount);
        statusLabel.setText("●" + status);
        
        if (statusLabel != null) {
            SwingUtilities.invokeLater(() -> {
                String text = statusLabel.getText().trim();
                System.out.println("Text: " + text); // Debugging
                if (text.equals("●Completed")) {
                    statusLabel.setForeground(Color.GREEN);
                } else if (text.equals("●Rejected")) {
                    statusLabel.setForeground(Color.RED);
                }
                statusLabel.getParent().revalidate();
                statusLabel.getParent().repaint();
            });
        }
        
        List<Order> orders = read.readOrder(order.getFilepath());
        for (Order order : orders){
            if(order.getDeliveryID().equals(deliveryId)){
                this.address = order.getOrderTypeDetails();
            }
        }
        addressLabel.setText(address);
    }
    
